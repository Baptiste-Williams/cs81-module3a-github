// 1. Returns a greeting message
function greet(name) {
  return "Hello, " + name + "!";
}

// 2. Adds two numbers
function add(a, b) {
  return a + b;
}

// 3. Checks if a number is even
function isEven(num) {
  return num % 2 === 0;
}

// 4. Converts Celsius to fahrenheit 
function toFahrenheit(celsius) {
  return (celsius * 9/5) + 32;
}

// 5. Returns the length of a string
function getLength(str) {
  return str. length;
}


